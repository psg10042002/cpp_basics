#include<bits/stdc++.h>
using namespace std;
int main()
{
	cout<<'c'+1<<endl;
	int a=3;
	cout<<a/2<<" "<<a/2.0<<endl;
	/*
	ranges:
	int -10^9 to 10^9
	long int -10^12 to 10^12
	long long int -10^18 to 10^18
	if range exceeds,overflow occurs
	fixed= scientific notation to num
	*/
	a=100000;
	int b=100000;
	double d= 1e24;
	cout<<fixed<<d<<endl;
	cout<<d<<endl;
	cout<<a*1LL*b<<endl;

	
}